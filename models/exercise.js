const mongoose = require('mongoose');

exerciseSchema = mongoose.Schema({
    userId: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    duration: {
        type: Number,
        required: true
    },
    date: {
        type: Date
    }
})

module.exports = mongoose.model('Exercise', exerciseSchema);